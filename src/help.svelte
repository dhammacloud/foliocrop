<pre><span class="title">Folio Crop 圖版裁切</span>
Zip 打開壓縮檔(Alt-Z)  💾 下載座標檔(Alt-S) ♻️  重置圖框 (Alt-R)  ➖ 刪除圖框(Alt-D) 目前圖框數
Alt-O 打開文件夾

<a href="https://www.youtube.com/watch?v=UvtJITtLz1c" target="_new">操作示範影片</a>

點任何一個圖框，序號變紅色時，表示選取，再點一下取消選取。
上下左右鍵移動圖框（選取中或全部），同時按Alt最小移動，按Ctrl更快移動。
按圖框左邊和上邊單獨移動。圖框第一行和最後一行文字應對齊（卷末校注可以大致對齊）。
沒有內文和注釋的圖拍，如封面裡，須刪掉圖框。
設置好圖框後，Enter 到下一拍。

限 Full HD 解析度(1920x1080)。
建議一開始按F11進入全螢幕模式。
按 Ctrl + - 調整好瀏覽器的解析度，無須經常改動。

